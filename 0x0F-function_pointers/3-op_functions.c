/**
 * op_add - fn to add
 * op_sub - fn to sub
 * op_mul - fn to multiply
 * op_div - fn to divide
 * op_mod - fn to get remainder
 * @a: input num
 * @b: input num
 * Return: value
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - fn to sub
 * @a: input num
 * @b: input num
 * Return: value
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - fn to mul
 * @a: input num
 * @b: input num
 * Return: value
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - fn to divide
 * @a: input num
 * @b: input num
 * Return: value
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - fn to get rem
 * @a: input num
 * @b: input num
 * Return: value
*/

int op_mod(int a, int b)
{
	return (a % b);
}
